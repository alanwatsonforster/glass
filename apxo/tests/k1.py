# Turn rate.

starttestsetup()
A1 = aircraft("A1", "AF", "F-100C", "A2-2025", "N", 5, 5.0, "CL")
endtestsetup()
startgameturn()
A1.move("LVL", "M", "H/BR,ETR/H,H,H,H")
A1._assert("A2-2020       N     5", 4.5)
endgameturn()

starttestsetup()
A1 = aircraft("A1", "AF", "F-100C", "A2-2025", "N", 5, 5.0, "CL")
A1.takedamage("L")
endtestsetup()
startgameturn()
A1.move("LVL", "M", "H/BR,ETR/H,H,H,H")
asserterror("damage limits the turn rate to BT.")
startgameturn()
A1.move("LVL", "M", "H/BR,BTR/H,H,H,H")
A1._assert("A2-2020       N     5", 5.0)
endgameturn()

